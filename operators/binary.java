package operators;
public class binary {
    public static void main(String[] args) {
        int A = 10;
        int B = 5;
        
        System.out.println("ADD: "+(A+B));
        
        System.out.println("SUBSTRACT: "+(A-B));
        
        System.out.println("MULTIPLICATION: "+(A*B));
        
        System.out.println("DIVISION: "+(A/B));
        
        System.out.println("REMAINDER: "+ (A % B));
    }
}
